## About Laravel</p>

<br>Laravel is a web application framework with expressive, elegant syntax. We believe development must be an enjoyable and creative experience to be truly fulfilling. Laravel takes the pain out of development by easing common tasks used in many web projects, such as:

<ul>- [Simple, fast routing engine](https://laravel.com/docs/routing).</ul>
<ul>- [Powerful dependency injection container](https://laravel.com/docs/container).</ul>
<ul>- Multiple back-ends for [session](https://laravel.com/docs/session) and [cache](https://laravel.com/docs/cache) storage.</ul>
<ul>- Expressive, intuitive [database ORM](https://laravel.com/docs/eloquent).</ul>
<ul>- Database agnostic [schema migrations](https://laravel.com/docs/migrations).</ul>
<ul>- [Robust background job processing](https://laravel.com/docs/queues).</ul>
<ul>- [Real-time event broadcasting](https://laravel.com/docs/broadcasting).</ul>


Laravel is accessible, powerful, and provides tools required for large, robust applications.</p>

